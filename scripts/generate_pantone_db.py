def make_uncoated(c: float, m: float, y: float, k: float) -> tuple[float, float, float, float]:
    """Uncoated stock absorbs more ink -> slightly darker, warmer."""
    return (
        clamp(c * 0.94 + 1),
        clamp(m * 0.96 + 1.5),
        clamp(y * 0.97 + 1),
        clamp(k + 3),
    )


def make_matte(c: float, m: float, y: float, k: float) -> tuple[float, float, float, float]:
    """Matte is between coated and uncoated."""
    return (
        clamp(c * 0.97 + 0.5),
        clamp(m * 0.98 + 0.8),
        clamp(y * 0.985 + 0.5),
        clamp(k + 1.5),
    )


